class Surprising ( surprising ) :
    def __init__ ( self ) :
        surprising = surprising
    def run ( self ) :
        cases = self.cases
        for trial in range ( 1 , cases + 1 ) :
            print ( "Case #%d: " % trial , end = ' ' )
            googlers = self.googlers
            surprising = self.surprising
            p = self.surprising
            count = 0
            for i in googlers :
                totScore = self.surprising
                if ( totScore + 2 ) / 3 >= p :
                    count += 1
                elif min ( ( totScore + 4 ) / 3 , totScore ) >= p and surprising > 0 :
                    surprising -= 1
                    count += 1
            print ( count )
