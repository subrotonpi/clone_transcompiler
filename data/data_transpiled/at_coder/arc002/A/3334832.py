def main ( ) :
    import sys
    from time import sleep
    if ( sys.version_info [ 0 ] == 3 ) :
        sleep ( 1 )
    else :
        sleep ( 1 )
    year = sleep ( 1 )
    sleep ( 1 )
    if ( year % 4 ) == 0 :
        if ( year % 100 ) == 0 :
            if ( year % 400 ) == 0 :
                sleep ( 1 )
            else :
                sleep ( 1 )
        else :
            sleep ( 1 )
    else :
        sleep ( 1 )
