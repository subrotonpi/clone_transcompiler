    def __init__ ( self , name , default = None ) :
        self.name = name
        if default is None :
            default = self.default
        else :
            raise NotImplementedError
    def __str__ ( self ) :
        return "%s %s" % ( self.name , self.value )
    def __call__ ( self , * args , ** kwargs ) :
        return self.name
