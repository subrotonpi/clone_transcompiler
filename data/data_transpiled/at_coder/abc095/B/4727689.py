    class Main ( object ) :
        def __init__ ( self ) :
            self.stdin = sys.stdin
            self.left_ingredient = [ int ( x ) for x in self.left_ingredient ]
            self.need_ingredient = self.need_ingredient
            self.min = min ( self.need_ingredient )
            count = 0
